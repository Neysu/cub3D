int	charmap(char c)
{
	if (c == '1' || c == '0' || c == 'N'
		|| c == 'W' || c == 'S' || c == 'E' || c == ' ')
		return (1);
	return (0);
}

int	isplayer(char c)
{
	if (c == 'N' || c == 'W' || c == 'S' || c == 'E')
		return (1);
	return (0);
}
